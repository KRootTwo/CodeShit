public class Student {
    private String name;
    private String reg;
    private int marks;

    public void setName(String name) {
        this.name = name;
    }
    public void setReg(String reg) {
        this.reg = reg;
    }
    public void setMarks(int marks) {
        this.marks = marks;
    }

    public String getName() {
        return name;
    }
    public String getReg() {
        return reg;
    }
    public int getMarks() {
        return marks;
    }

}
